package kr.nanoit.education.server;

public class ServerInfo {
    private String IP;
    private String Port;

    public String getIP() {
        return IP;
    }

    public String getPort() {
        return Port;
    }

    public void setIP(String IP) {
        this.IP = IP;
    }

    public void setPort(String port) {
        Port = port;
    }
}